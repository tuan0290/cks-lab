    def _write_lab_files(self, lab: LabExercise) -> None:
        """Write lab files to disk.

        Args:
            lab: LabExercise object with content to write
        """
        lab_path = Path(lab.directory_path)

        # Write README.md
        readme_path = lab_path / "README.md"
        readme_path.write_text(lab.readme_content, encoding="utf-8")
        logger.info(f"Created README.md: {readme_path}")

        # Write solution.md
        solution_path = lab_path / "solution" / "solution.md"
        solution_path.write_text(lab.solution_content, encoding="utf-8")
        logger.info(f"Created solution.md: {solution_path}")

        # Create placeholder scripts (will be replaced by ScriptGenerator)
        for script_name in ["setup.sh", "cleanup.sh", "verify.sh"]:
            script_path = lab_path / script_name
            if not script_path.exists():
                script_path.write_text(
                    f"#!/bin/bash\n# TODO: Implement {script_name}\n",
                    encoding="utf-8",
                )
                script_path.chmod(0o755)
                logger.info(f"Created placeholder {script_name}: {script_path}")

    def assign_difficulty(self, skill_topic: SkillTopic) -> str:
        """Determine difficulty level.

        Args:
            skill_topic: Skill topic to assess

        Returns:
            Difficulty level: "Easy", "Medium", or "Hard"
        """
        # If already assigned, return it
        if skill_topic.difficulty:
            return skill_topic.difficulty

        # Calculate complexity score
        num_commands = len(skill_topic.commands)
        num_yaml = len(skill_topic.yaml_manifests)
        num_prereqs = len(skill_topic.prerequisites)

        # Get criteria from config
        easy_criteria = self.config.get_difficulty_criteria("easy")
        medium_criteria = self.config.get_difficulty_criteria("medium")

        # Determine difficulty
        if (
            num_commands <= easy_criteria["max_commands"]
            and num_yaml <= easy_criteria["max_yaml_files"]
        ):
            return "Easy"
        elif (
            num_commands <= medium_criteria["max_commands"]
            and num_yaml <= medium_criteria["max_yaml_files"]
        ):
            return "Medium"
        else:
            return "Hard"

    def estimate_time(self, skill_topic: SkillTopic) -> int:
        """Estimate completion time in minutes.

        Args:
            skill_topic: Skill topic to estimate

        Returns:
            Estimated time in minutes
        """
        # If already estimated, return it
        if skill_topic.estimated_time:
            return skill_topic.estimated_time

        # Base time
        base_time = 5

        # Add time based on complexity
        time = base_time
        time += len(skill_topic.commands) * 1  # 1 min per command
        time += len(skill_topic.yaml_manifests) * 2  # 2 min per YAML
        time += len(skill_topic.prerequisites) * 2  # 2 min per prerequisite

        # Adjust for difficulty
        difficulty = self.assign_difficulty(skill_topic)
        if difficulty == "Hard":
            time = int(time * 1.5)
        elif difficulty == "Medium":
            time = int(time * 1.2)

        return time

    def update_main_readme(
        self, lab: LabExercise, main_readme_path: str
    ) -> None:
        """Update main README.md with new lab entry.

        Args:
            lab: LabExercise to add to README
            main_readme_path: Path to main README.md
        """
        readme_path = Path(main_readme_path)

        if not readme_path.exists():
            logger.warning(f"Main README not found: {main_readme_path}")
            return

        # Read existing content
        content = readme_path.read_text(encoding="utf-8")

        # Find domain section
        domain_header = f"## Domain {lab.domain_number}"
        if domain_header not in content:
            logger.warning(f"Domain section not found in README: {domain_header}")
            return

        # Create lab entry
        lab_entry = f"| {lab.lab_id} | {lab.topic_name} | {lab.skill_topic.difficulty} |\n"

        # Find insertion point (after domain table header)
        lines = content.split("\n")
        insert_index = -1

        for i, line in enumerate(lines):
            if line.startswith(domain_header):
                # Find table header
                for j in range(i, min(i + 10, len(lines))):
                    if lines[j].startswith("|") and "Lab" in lines[j]:
                        # Found table, insert after separator line
                        insert_index = j + 2
                        break
                break

        if insert_index > 0:
            lines.insert(insert_index, lab_entry.rstrip())
            updated_content = "\n".join(lines)
            readme_path.write_text(updated_content, encoding="utf-8")
            logger.info(f"Updated main README: {main_readme_path}")
        else:
            logger.warning("Could not find insertion point in README")

    def batch_generate(
        self,
        skill_topics: List[SkillTopic],
        domains: Dict[int, Domain],
        output_dir: Optional[str] = None,
    ) -> tuple[List[LabExercise], List[tuple[SkillTopic, str]]]:
        """Generate multiple labs in batch.

        Args:
            skill_topics: List of skill topics to generate labs for
            domains: Dictionary mapping domain numbers to Domain objects
            output_dir: Optional custom output directory

        Returns:
            Tuple of (successful_labs, failed_labs_with_errors)
        """
        logger.info(f"Starting batch generation of {len(skill_topics)} labs")

        successful = []
        failed = []

        for topic in skill_topics:
            try:
                # Get domain for this topic
                domain = domains.get(topic.domain_number)
                if not domain:
                    raise ValueError(f"Domain {topic.domain_number} not found")

                # Generate lab
                lab = self.generate_lab(topic, domain, output_dir)
                successful.append(lab)

            except Exception as e:
                logger.error(f"Failed to generate lab for {topic.id}: {e}")
                failed.append((topic, str(e)))

        logger.info(
            f"Batch generation complete: {len(successful)} successful, {len(failed)} failed"
        )

        return successful, failed
