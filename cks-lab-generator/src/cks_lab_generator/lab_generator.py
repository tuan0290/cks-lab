    def _generate_requirements(self, skill_topic: SkillTopic) -> List[str]:
        """Generate lab requirements.

        Args:
            skill_topic: Skill topic for the lab

        Returns:
            List of requirements
        """
        requirements = []

        # Add requirements based on commands and YAML
        if skill_topic.commands:
            requirements.append(
                f"Execute the necessary commands to configure {skill_topic.name}"
            )

        if skill_topic.yaml_manifests:
            requirements.append(
                f"Create and apply the required Kubernetes manifests"
            )

        # Add verification requirement
        requirements.append("Verify the configuration is working correctly")

        # Add documentation requirement for complex labs
        if skill_topic.difficulty == "Hard":
            requirements.append(
                "Document any troubleshooting steps you performed"
            )

        return requirements

    def _generate_instructions(
        self, skill_topic: SkillTopic
    ) -> List[tuple[str, str]]:
        """Generate step-by-step instructions.

        Args:
            skill_topic: Skill topic for the lab

        Returns:
            List of (step_title, step_content) tuples
        """
        instructions = []

        # Step 1: Setup
        instructions.append(
            (
                "Set up the lab environment",
                "Run the setup script to create the initial resources:\n\n"
                "```bash\n./setup.sh\n```\n\n"
                "This will create the necessary namespace and base resources.",
            )
        )

        # Step 2: Main task based on topic
        main_task = self._generate_main_task(skill_topic)
        instructions.append(("Complete the main task", main_task))

        # Step 3: Verification
        instructions.append(
            (
                "Verify your solution",
                "Use the verification script to check if your configuration is correct:\n\n"
                "```bash\n./verify.sh\n```\n\n"
                "Review any failed checks and make corrections as needed.",
            )
        )

        return instructions

    def _generate_main_task(self, skill_topic: SkillTopic) -> str:
        """Generate main task description.

        Args:
            skill_topic: Skill topic for the lab

        Returns:
            Main task description
        """
        task_parts = []

        # Add context if available
        if skill_topic.context:
            # Extract relevant parts of context
            context_lines = skill_topic.context.split("\n")
            relevant_context = [
                line for line in context_lines if line.strip() and not line.startswith("#")
            ][:3]
            if relevant_context:
                task_parts.append("\n".join(relevant_context))
                task_parts.append("")

        # Add command examples if available
        if skill_topic.commands:
            task_parts.append("Execute the following commands:")
            task_parts.append("")
            for cmd in skill_topic.commands[:3]:  # Limit to first 3 commands
                task_parts.append(f"```bash\n{cmd.command}\n```")
                if cmd.description:
                    task_parts.append(f"*{cmd.description}*")
                task_parts.append("")

        # Add YAML examples if available
        if skill_topic.yaml_manifests:
            task_parts.append("Create and apply the following Kubernetes resources:")
            task_parts.append("")
            for manifest in skill_topic.yaml_manifests[:2]:  # Limit to first 2
                task_parts.append(f"```yaml\n{manifest.content}\n```")
                if manifest.description:
                    task_parts.append(f"*{manifest.description}*")
                task_parts.append("")

        # Default task if no specific content
        if not task_parts:
            task_parts.append(
                f"Configure {skill_topic.name} according to Kubernetes security best practices."
            )
            task_parts.append("")
            task_parts.append(
                "Refer to the official Kubernetes documentation for detailed configuration options."
            )

        return "\n".join(task_parts)

