    def __init__(self, config: Config) -> None:
        """Initialize lab generator.

        Args:
            config: Configuration instance
        """
        self.config = config

    def generate_lab(
        self, skill_topic: SkillTopic, domain: Domain, output_dir: Optional[str] = None
    ) -> LabExercise:
        """Generate complete lab exercise.

        Args:
            skill_topic: Skill topic to generate lab for
            domain: Domain the skill topic belongs to
            output_dir: Optional custom output directory

        Returns:
            LabExercise object with all generated content
        """
        logger.info(f"Generating lab for topic {skill_topic.id}: {skill_topic.name}")

        # Determine output directory
        if output_dir is None:
            output_dir = str(self.config.get_output_directory())

        # Create lab directory
        lab_dir = self.create_lab_directory(skill_topic, domain, output_dir)

        # Generate content
        readme_content = self.generate_readme(skill_topic, domain)
        solution_content = self.generate_solution(skill_topic, domain)

        # Create LabExercise object
        lab_id = Path(lab_dir).name
        lab = LabExercise(
            lab_id=lab_id,
            domain_number=domain.number,
            skill_topic=skill_topic,
            directory_path=lab_dir,
            readme_content=readme_content,
            solution_content=solution_content,
            setup_script="",  # Will be generated by ScriptGenerator
            cleanup_script="",  # Will be generated by ScriptGenerator
            verify_script="",  # Will be generated by ScriptGenerator
            created_date=datetime.now().isoformat(),
        )

        # Write files
        self._write_lab_files(lab)

        logger.info(f"Lab generated successfully: {lab_dir}")
        return lab

    def create_lab_directory(
        self, skill_topic: SkillTopic, domain: Domain, output_dir: str
    ) -> str:
        """Create lab directory with proper naming.

        Uses the topic ID directly as the lab number (e.g., topic 1.2 → lab-1.2-...)
        making the operation idempotent - same topic always maps to same directory.

        Args:
            skill_topic: Skill topic for the lab
            domain: Domain the skill topic belongs to
            output_dir: Base output directory

        Returns:
            Path to created lab directory
        """
        # Get domain directory name
        domain_dir_name = f"{domain.number:02d}-{self._to_kebab_case(domain.name)}"
        domain_path = Path(output_dir) / domain_dir_name

        # Create domain directory if it doesn't exist
        domain_path.mkdir(parents=True, exist_ok=True)

        # Use topic ID as lab number (e.g., "1.2" → minor number "2")
        # This makes generation idempotent - same topic always same directory
        topic_parts = skill_topic.id.split(".")
        lab_number = topic_parts[1] if len(topic_parts) > 1 else "1"

        # Use English name from map, fallback to kebab of original name
        topic_kebab = self.TOPIC_NAME_MAP.get(
            skill_topic.id, self._to_kebab_case(skill_topic.name)
        )
        lab_dir_name = f"lab-{domain.number}.{lab_number}-{topic_kebab}"
        lab_path = domain_path / lab_dir_name

        # Create lab directory and solution subdirectory
        lab_path.mkdir(parents=True, exist_ok=True)
        (lab_path / "solution").mkdir(exist_ok=True)

        logger.info(f"Created lab directory: {lab_path}")
        return str(lab_path)

    def _get_next_lab_number(self, domain_path: Path, domain_number: int) -> int:
        """Get next available lab number in domain.

        Args:
            domain_path: Path to domain directory
            domain_number: Domain number

        Returns:
            Next available lab number
        """
        if not domain_path.exists():
            return 1

        # Find all existing lab directories
        existing_labs = []
        pattern = re.compile(rf"lab-{domain_number}\.(\d+)-")

        for item in domain_path.iterdir():
            if item.is_dir() and item.name.startswith("lab-"):
                match = pattern.match(item.name)
                if match:
                    existing_labs.append(int(match.group(1)))

        # Return next number
        if existing_labs:
            return max(existing_labs) + 1
        return 1

    def _to_kebab_case(self, text: str) -> str:
        """Convert text to kebab-case.

        Args:
            text: Text to convert

        Returns:
            Kebab-case string
        """
        # Remove special characters and replace spaces with hyphens
        text = re.sub(r"[^\w\s-]", "", text)
        text = re.sub(r"[\s_]+", "-", text)
        return text.lower().strip("-")

