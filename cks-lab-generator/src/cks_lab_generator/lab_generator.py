        # Solution Steps
        lines.append("## Solution Steps")
        lines.append("")

        # Generate solution steps
        solution_steps = self._generate_solution_steps(skill_topic)
        for i, (step_title, step_content) in enumerate(solution_steps, 1):
            lines.append(f"### Step {i}: {step_title}")
            lines.append("")
            lines.append(step_content)
            lines.append("")

        # Verification
        lines.append("## Verification")
        lines.append("")
        lines.append("After completing all steps, verify your solution:")
        lines.append("")
        lines.append("```bash")
        lines.append("./verify.sh")
